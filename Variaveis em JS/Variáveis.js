// Variaveis em JavaScript

// Variável "var", opcionalmente pode ser declarada sem valor ou com valor inicial
var nome = 'José';
var idade;

console.log('Valor da Variavel "var":');
console.log(nome);


// Variável "let", opcionalmente pode ser declarada sem valor ou com valor inicial
let sobrenome = 'Silva';
let altura;

console.log('Valor da Variavel "let":');
console.log(sobrenome);

// Variável "const", deve ser declarada com valor inicial e não pode ser reatribuída
const PI = 3.14159;

console.log('Valor da Variavel "const":');
console.log(PI);